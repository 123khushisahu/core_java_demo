package p1;

public class Parent {
public void show() {
	System.out.println("Show method 0 parameter");
}
//overloading
public void show(int a,int b) {
	System.out.println("a"+a);
	
	
}

public void show(int a,int b,int c) {
	System.out.println("a"+a);
	System.out.println("b"+b);
	System.out.println("c"+c);
}


   

}
